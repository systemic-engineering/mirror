#[cfg(test)]
mod tests {
    use super::*;
    use crate::mirror_runtime::MirrorRuntime;
    use std::path::PathBuf;

    fn mirror_target() -> TargetConfig {
        TargetConfig {
            name: "mirror".into(),
            source: Some("boot".into()),
            lens: Some("@code/rust".into()),
            output_path: Some("rust/mirror/".into()),
            glob: None,
            grammars: vec!["@prism".into(), "@meta".into()],
        }
    }

    #[test]
    fn generate_cargo_toml_has_package_name() {
        let target = mirror_target();
        let toml = generate_cargo_toml(&target);
        assert!(toml.contains("name = \"mirror\""), "got:\n{}", toml);
    }

    #[test]
    fn generate_cargo_toml_has_dependencies() {
        let target = mirror_target();
        let toml = generate_cargo_toml(&target);
        assert!(toml.contains("[dependencies]"), "got:\n{}", toml);
    }

    #[test]
    fn generate_cargo_toml_has_edition() {
        let target = mirror_target();
        let toml = generate_cargo_toml(&target);
        assert!(toml.contains("edition = \"2021\""), "got:\n{}", toml);
    }

    #[test]
    fn generate_lib_rs_has_header() {
        let target = mirror_target();
        let lib = generate_lib_rs(&target, &[]);
        assert!(lib.contains("Generated by mirror craft"), "got:\n{}", lib);
    }

    #[test]
    fn generate_lib_rs_from_boot_prism() {
        let runtime = MirrorRuntime::new();
        let path = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("boot/00-prism.mirror");
        let compiled = runtime.compile_file(&path).unwrap();
        let target = mirror_target();

        let lib = generate_lib_rs(&target, &[compiled.fragment]);
        assert!(
            lib.contains("Generated by mirror craft"),
            "should have header, got:\n{}",
            lib
        );
        // Boot prism is structural — it produces comments, not pub items.
        // The emitter correctly renders what the grammar contains.
        assert!(!lib.is_empty(), "should produce non-empty output");
    }

    #[test]
    fn generate_lib_rs_from_boot_meta() {
        let runtime = MirrorRuntime::new();
        let path = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("boot/01-meta.mirror");
        let compiled = runtime.compile_file(&path).unwrap();
        let target = mirror_target();

        let lib = generate_lib_rs(&target, &[compiled.fragment]);
        assert!(
            lib.contains("pub"),
            "meta should produce pub items, got:\n{}",
            lib
        );
    }

    #[test]
    fn generate_crate_produces_both_files() {
        let target = mirror_target();
        let output = generate_crate(&target, &[]);
        assert!(!output.cargo_toml.is_empty());
        assert!(!output.lib_rs.is_empty());
    }

    // -----------------------------------------------------------------------
    // Bridge module generation — bootstrap closure
    // -----------------------------------------------------------------------

    fn boot_dir() -> PathBuf {
        PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("boot")
    }

    fn tempdir_for_test(name: &str) -> PathBuf {
        let dir = std::env::temp_dir().join(format!("mirror-test-{}-{}", name, std::process::id()));
        let _ = std::fs::create_dir_all(&dir);
        dir
    }

    #[test]
    fn generate_bridge_module_from_boot() {
        let runtime = MirrorRuntime::new();
        let store = tempdir_for_test("gen_bridge");
        let boot = runtime.compile_boot_dir(&boot_dir(), &store).unwrap();

        let fragments: Vec<_> = boot.resolved.values().map(|c| c.fragment.clone()).collect();

        let bridge = generate_bridge_module(&fragments);

        eprintln!("--- bridge module ---");
        eprintln!("{}", bridge);
        eprintln!("---");

        // Must contain derive(Prism) for each grammar
        assert!(
            bridge.contains("#[derive(Debug, Clone, Prism)]"),
            "must have derive, got:\n{}",
            bridge
        );
        assert!(
            bridge.contains("#[oid(\"@meta\")]"),
            "must have @meta oid, got:\n{}",
            bridge
        );
        assert!(
            bridge.contains("pub struct MetaGrammar;"),
            "must have MetaGrammar, got:\n{}",
            bridge
        );

        // Cleanup
        let _ = std::fs::remove_dir_all(&store);
    }

    #[test]
    fn generate_bridge_module_contains_all_boot_grammars() {
        let runtime = MirrorRuntime::new();
        let store = tempdir_for_test("gen_bridge_all");
        let boot = runtime.compile_boot_dir(&boot_dir(), &store).unwrap();

        let fragments: Vec<_> = boot.resolved.values().map(|c| c.fragment.clone()).collect();

        let bridge = generate_bridge_module(&fragments);

        // These grammars should all appear from the boot directory
        let expected_grammars = ["@meta", "@code/rust", "@property", "@actor"];
        for g in &expected_grammars {
            assert!(
                bridge.contains(&format!("#[oid(\"{}\")]", g)),
                "bridge must contain {}, got:\n{}",
                g,
                bridge
            );
        }

        let _ = std::fs::remove_dir_all(&store);
    }

    #[test]
    fn grammar_name_to_struct_simple() {
        assert_eq!(grammar_name_to_struct("@meta"), "MetaGrammar");
    }

    #[test]
    fn grammar_name_to_struct_path() {
        assert_eq!(grammar_name_to_struct("@code/rust"), "CodeRustGrammar");
    }

    #[test]
    fn grammar_name_to_struct_kebab() {
        assert_eq!(grammar_name_to_struct("@meta-action"), "MetaActionGrammar");
    }

    #[test]
    fn grammar_name_to_static_simple() {
        assert_eq!(grammar_name_to_static("@meta"), "META_OID");
    }

    #[test]
    fn grammar_name_to_static_path() {
        assert_eq!(grammar_name_to_static("@code/rust"), "CODE_RUST_OID");
    }

    #[test]
    fn bridge_module_has_grammar_oid_fn() {
        let runtime = MirrorRuntime::new();
        let store = tempdir_for_test("gen_bridge_fn");
        let boot = runtime.compile_boot_dir(&boot_dir(), &store).unwrap();

        let fragments: Vec<_> = boot.resolved.values().map(|c| c.fragment.clone()).collect();

        let bridge = generate_bridge_module(&fragments);
        assert!(
            bridge.contains("pub fn grammar_oid("),
            "must have grammar_oid function"
        );
        assert!(
            bridge.contains("LazyLock<Oid>"),
            "must have cached LazyLock statics"
        );
        assert!(
            bridge.contains("GRAMMAR_NAMES"),
            "must have GRAMMAR_NAMES constant"
        );

        let _ = std::fs::remove_dir_all(&store);
    }

    #[test]
    fn generated_bridge_compiles_as_rust() {
        // The generated bridge should be valid Rust syntax (basic check)
        let runtime = MirrorRuntime::new();
        let store = tempdir_for_test("gen_bridge_syntax");
        let boot = runtime.compile_boot_dir(&boot_dir(), &store).unwrap();

        let fragments: Vec<_> = boot.resolved.values().map(|c| c.fragment.clone()).collect();

        let bridge = generate_bridge_module(&fragments);

        // Every line should be a valid Rust construct or comment/blank
        for line in bridge.lines() {
            let trimmed = line.trim();
            if trimmed.is_empty()
                || trimmed.starts_with("//")
                || trimmed.starts_with("use ")
                || trimmed.starts_with("static ")
                || trimmed.starts_with("pub fn ")
                || trimmed.starts_with("pub const ")
                || trimmed.starts_with("match ")
                || trimmed.starts_with('"')
                || trimmed.starts_with("_ =>")
                || trimmed == "}"
                || trimmed == "})"
                || trimmed == "];"
            {
                continue;
            }
            assert!(
                trimmed.starts_with("#[") || trimmed.starts_with("pub struct"),
                "unexpected line in bridge: {:?}",
                line
            );
        }

        let _ = std::fs::remove_dir_all(&store);
    }
}
