//! generate_crate — Generate Rust crate files from a SpecConfig target.
//!
//! Given a `TargetConfig` and compiled grammar fragments, generates:
//! - `Cargo.toml`
//! - `src/lib.rs` with Rust types derived from the grammars
//! - Optional per-grammar module files

use crate::declaration::MirrorFragment;
use crate::spec::TargetConfig;

// ---------------------------------------------------------------------------
// Output types
// ---------------------------------------------------------------------------

/// The output of crate generation: files ready to write to disk.
pub struct CrateOutput {
    /// Generated `Cargo.toml` content.
    pub cargo_toml: String,
    /// Generated `src/lib.rs` content.
    pub lib_rs: String,
    /// Additional module files: `(filename, content)`.
    pub modules: Vec<(String, String)>,
}

// ---------------------------------------------------------------------------
// Generation
// ---------------------------------------------------------------------------

/// Generate a `Cargo.toml` from a target configuration.
pub fn generate_cargo_toml(target: &TargetConfig) -> String {
    format!(
        r#"[package]
name = "{name}"
version = "0.1.0"
edition = "2021"

# Generated by mirror craft
# Do not edit — this file is derived from mirror.spec

[dependencies]
"#,
        name = target.name,
    )
}

/// Generate `src/lib.rs` from compiled grammar fragments.
pub fn generate_lib_rs(target: &TargetConfig, grammars: &[MirrorFragment]) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "// Generated by mirror craft — target \"{}\"\n",
        target.name
    ));
    out.push_str("// Do not edit — this file is derived from .mirror source\n\n");

    if grammars.is_empty() {
        // Empty crate with just the grammars listed as comments
        for g in &target.grammars {
            out.push_str(&format!("// grammar: {}\n", g));
        }
    } else {
        // Emit Rust from each grammar fragment
        for frag in grammars {
            let rust = crate::emit_rust::emit_rust_fragment(frag);
            // Strip the emit_rust header (it adds its own) — we already have ours
            let body = strip_generated_header(&rust);
            out.push_str(body);
            out.push('\n');
        }
    }

    out
}

/// Generate a full crate from a target and its compiled grammars.
pub fn generate_crate(target: &TargetConfig, grammars: &[MirrorFragment]) -> CrateOutput {
    CrateOutput {
        cargo_toml: generate_cargo_toml(target),
        lib_rs: generate_lib_rs(target, grammars),
        modules: Vec::new(),
    }
}

/// Generate a bridge module: Prism-derived structs for each grammar in boot.
///
/// Each grammar `@X` becomes a unit struct `XGrammar` with `#[derive(Prism)]`
/// and `#[oid("@X")]`. These types close the bootstrap loop: the parser
/// compiles boot grammars, the codegen generates Prism types from them,
/// and the parser can resolve to those types by Oid.
pub fn generate_bridge_module(grammars: &[MirrorFragment]) -> String {
    let mut out = String::new();
    out.push_str("// Generated by mirror craft boot\n");
    out.push_str("// Do not edit — this file is derived from boot grammars\n\n");
    out.push_str("use prism as prism_core;\n");
    out.push_str("use prism::{Addressable, Oid};\n");
    out.push_str("use prism::DerivePrism as Prism;\n\n");
    out.push_str("use std::sync::LazyLock;\n\n");

    let mut names: Vec<&str> = Vec::new();
    collect_grammar_names_from_fragments(grammars, &mut names);
    // Deduplicate while preserving order
    let mut seen = std::collections::HashSet::new();
    names.retain(|n| seen.insert(*n));

    for name in &names {
        let struct_name = grammar_name_to_struct(name);
        out.push_str("#[derive(Debug, Clone, Prism)]\n");
        out.push_str(&format!("#[oid(\"{}\")]\n", name));
        out.push_str(&format!("pub struct {};\n\n", struct_name));
    }

    // Emit cached LazyLock statics
    out.push_str("// Cached Oids — computed once via LazyLock, then O(1) clone.\n");
    for name in &names {
        let struct_name = grammar_name_to_struct(name);
        let static_name = grammar_name_to_static(name);
        out.push_str(&format!(
            "static {}: LazyLock<Oid> = LazyLock::new(|| {}.oid());\n",
            static_name, struct_name
        ));
    }
    out.push('\n');

    // Emit grammar_oid lookup function
    out.push_str("/// All boot grammar Oids, keyed by name. O(1) lookup after first call.\n");
    out.push_str("pub fn grammar_oid(name: &str) -> Option<Oid> {\n");
    out.push_str("    match name {\n");
    for name in &names {
        let static_name = grammar_name_to_static(name);
        out.push_str(&format!(
            "        \"{}\" => Some({}.clone()),\n",
            name, static_name
        ));
    }
    out.push_str("        _ => None,\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // Emit GRAMMAR_NAMES constant
    out.push_str("/// All known grammar names in boot order.\n");
    out.push_str("pub const GRAMMAR_NAMES: &[&str] = &[\n");
    for name in &names {
        out.push_str(&format!("    \"{}\",\n", name));
    }
    out.push_str("];\n");

    out
}

/// Recursively collect grammar names (starting with `@`) from fragment trees.
fn collect_grammar_names_from_fragments<'a>(
    fragments: &'a [MirrorFragment],
    names: &mut Vec<&'a str>,
) {
    use crate::declaration::MirrorFragmentExt;
    for frag in fragments {
        let data = frag.mirror_data();
        // Skip bare `@` — it's a structural reference, not a grammar name
        if data.name.starts_with('@') && data.name.len() > 1 {
            names.push(&data.name);
        }
        collect_grammar_names_from_fragments(frag.mirror_children(), names);
    }
}

/// Convert a grammar name like `@code/rust` to a struct name like `CodeRustGrammar`.
fn grammar_name_to_struct(name: &str) -> String {
    let stripped = name.trim_start_matches('@');
    let pascal = stripped
        .split(['/', '_', '-'])
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                None => String::new(),
                Some(first) => {
                    let mut s = first.to_uppercase().to_string();
                    s.extend(chars);
                    s
                }
            }
        })
        .collect::<String>();
    format!("{}Grammar", pascal)
}

/// Convert a grammar name like `@code/rust` to a static name like `CODE_RUST_OID`.
fn grammar_name_to_static(name: &str) -> String {
    let stripped = name.trim_start_matches('@');
    let upper = stripped.replace(['/', '-'], "_").to_uppercase();
    format!("{}_OID", upper)
}

/// Strip the "Generated by mirror craft" header that emit_rust adds,
/// since we add our own header in generate_lib_rs.
fn strip_generated_header(source: &str) -> &str {
    let mut pos = 0;
    for line in source.lines() {
        if line.starts_with("// Generated by") || line.starts_with("// Do not edit") {
            pos += line.len() + 1; // +1 for newline
        } else {
            break;
        }
    }
    // Skip any trailing blank line after the header
    if pos < source.len() && source.as_bytes().get(pos) == Some(&b'\n') {
        pos += 1;
    }
    &source[pos..]
}

